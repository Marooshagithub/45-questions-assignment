function city_country(city, country) {
    return "".concat(city, " ,").concat(country);
}
var c1 = city_country('lahore', 'pakistan');
var c2 = city_country('madina', 'saudiarabia');
var c3 = city_country('istambol', 'turkey');
//using return function to return the values
console.log(c1);
console.log(c2);
console.log(c3);
